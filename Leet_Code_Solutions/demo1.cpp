#include <bits/stdc++.h>
using namespace std;

int main()
{
    vector<string> another;
    int count = 1;
    int n = 4;
    for (int i = 0; i < n; i++)
    {
        string curr = "";
        for (int j = 0; j < (2 * i + 1); j++)
        {
            if (j % 2 == 1)
            {
                curr += '*';
            }
            else
            {
                curr += to_string(count);
                count++;
            }
        }
        cout << curr << endl;
        another.push_back(curr);
    }

    for (int i = n - 1; i >= 0; i--)
    {
        cout << another[i] << endl;
    }
    return 0;
}